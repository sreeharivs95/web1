                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; height: 370px;   width:100%; line-height: 1.5; text-align: justify; ">Guidelines for Bus and Truck Drivers regarding towing and loading :
                                               
                                                <li style="line-height: 1.5;">You must not tow more than your license permits you to.</li>
                                                <li style="line-height: 1.5;">	You must not overload your vehicle or trailer. You should not tow a weight greater than that recommended by the manufacturer of your vehicle.</li>
                                                <li style="line-height: 1.5;">You must secure your load and it must not stick out dangerously.</li>
                                                <li style="line-height: 1.5;">	You should properly distribute the weight in your caravan or trailer with heavy items mainly over the axle(s) and ensure a downward load on the tow ball. This should avoid the possibility of swerving or snaking and going out of control. If this does happen, ease off the accelerator and reduce speed gently to regain control.</li>
                                                
                                                
