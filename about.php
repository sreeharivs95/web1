                                        <label style=" font-family: times new roman;  font-size: 18px; line-height: 1.5; ">Road traffic safety refers to methods and measures for reducing the risk of a person using the road network for being killed or seriously injured. The users of a road include pedestrians, cyclists, motorists, their passengers, and passengers of on-road public transport, mainly buses and trams. Best-practice road safety strategies focus upon the prevention of serious injury and death crashes in spite of human fallibility[1] (which is contrasted with the old road safety paradigm of simply reducing crashes assuming road user compliance with traffic regulations). Safe road design is now about providing a road environment which ensures vehicle speeds will be within the human tolerances for serious injury and death wherever conflict points exist.

                                            The basic strategy of a Safe System approach is to ensure that in the event of a crash, the impact energies remain below the threshold likely to produce either death or serious injury. This threshold will vary from crash scenario to crash scenario, depending upon the level of protection offered to the road users involved. For example, the chances of survival for an unprotected pedestrian hit by a vehicle diminish rapidly at speeds greater than 30 km/h, whereas for a properly restrained motor vehicle occupant the critical impact speed is 50 km/h (for side impact crashes) and 70 km/h (for head-on crashes).
