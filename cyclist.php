                                                   In the year 2003, 772 Cyclists were involved in accidents on Delhi Roads. 171 of them succumbed to their injuries while 646 were seriously injured. In order to be safe while cycling on roads of Delhi, keep the following tips in mind.
                                               </p>
                                            <br>
                                     
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; height: 179px;  width:100%; line-height: 1.5; text-align: justify; ">CYCLING ON ROADS
                                                <label style="font-size: 20px;">Before riding a cycle on roads see if you can</label> 
                                            <ul>
                                               
                                                    
                                                <li style="line-height: 1.5;">Ride in a straight line for 10 meters.</li>
                                                <li style="line-height: 1.5;">Stop suddenly without falling.</li>
                                                <li style="line-height: 1.5;">Give signals with one hand while riding.</li>
                                                <li style="line-height: 1.5;">Look back over your shoulders and take a right turn comfortably.</li>
                                                
                                                </ul>
                                            
                                            </div>
                                                    
                                                    <br>
                                         
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; width:100%; height: 189px; line-height: 1.5; text-align: justify; ">Before you go out on the road with your bicycle, ensure that;
                                                
                                                <ul>
                                                    
                                                    <li style="line-height: 1.5;">The bicycle is easily noticeable. It should be painted yellow, orange or white</li>
                                                    <li style="line-height: 1.5;">Reflectors are provided on spokes of wheels, on the pedals and on the front / rear mudguard.</li>
                                                    <li style="line-height:1.5;">Both the brakes are working properly</li>                                                                                                                                                            
                                                    <li style="line-height: 1.5;">The bell rings properly.</li>
                                                    <li style="line-height: 1.5;">The seat is so adjusted that your feet can touch the ground.</li>
                                                    
                                                </ul>  
                                                
                                                
                                                
                                                
                                            </div>
                                               
