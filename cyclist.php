                                                <label style="font-size:20px;">a. You should wear:</label>
                                                
                                                    
                                           
                                                        
                                                        <li style="line-height:1.5;">A cycle helmet made of light thermocol.</li>
                                                        <li style="line-height:1.5;">Appropriate clothes for cycling. Avoid clothes which may get tangled in the chain, or in a wheel or may obscure your lights.</li>
                                                        <li style="line-height:1.5;">Light-coloured or fluorescent clothing which helps other road users to see you even in dark and poorly lit roads</li>
                                                        <li style="line-height:1.5;">Reflective clothing and/or accessories (belt, arm or ankle bands) in the dark.</li>
                                                <label style="font-size:20px;">b. You should also follow these rules:</label>
                                           
                                               <li style="line-height:1.5;">Never ride with just one hand on the handle bar except when giving a signal</li>
                                               <li style="line-height:1.5;">Keep both feet on the pedals</li>
