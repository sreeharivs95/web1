                                                    &nbsp&nbsp;&nbsp&nbsp;&nbsp&nbsp; &#9899; keep close to the side of the road<BR><br>
It may be safer to cross the road well before a sharp right-hand bend so that oncoming traffic has a better chance of seeing you. Cross back after the bend.
                                                </b></p>
                                                <br>
                                           <h2 style=" float: left;">&#9733;</h2>
                                            <p style=" margin:  1% 0% 0% 5%; font-size: 20px; line-height: 1.5; text-align: justify; ">Help other road users to see you. Wear or carry something light-coloured, bright or fluorescent in poor daylight conditions. When it is dark, use reflective materials (example. armbands, sashes, waistcoats, jackets, footwear), which can be seen by drivers using headlights up to three times as far away as non-reflective materials.
                                                
                                                
                                            
