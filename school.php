                                            <h2 style="color:black;"> Guidelines for Children</h2>
                                            <br>
                                            
                                            <h2 style=" float: left;">&#9733;</h2><p style=" margin:  1% 0% 0% 5%; font-size: 20px; line-height: 1.5; text-align: justify; ">
                                                    The advice given below on crossing the road is especially for children. Children should be taught the safety code and should not be allowed on the road alone until they can understand and follow it properly. Children learn by example, so parents and teachers should always follow the Code proprely when going out with their children. They are responsible for deciding at what age children can use it safely by themselves.
 

                                               </p>
                                            <br><h2 style=" float: left;">&#9733;</h2>
                                     
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; height: 440px;  width:100%; line-height: 1.5; text-align: justify; ">While Walking to School
                                                 
                                            <ul>
                                               
                                                    
                                                <li style="line-height: 1.5;">Always walk on the footpath only. On roads without footpath, walk on the extreme right hand side of the roads.</li>
                                                <li style="line-height: 1.5;">Do not be impatient on the road. Do not rush or run on the road.</li>
                                                <li style="line-height: 1.5;">Cross only at Zebra crossings, traffic signals, subways, foot over-bridges. Where such facilities do not exist, look for a safe place to cross.</li>
                                                <li style="line-height: 1.5;">At the signal lights, cross only on a clear green signal. If an intersection is controlled by a policeman, traffic warden or RSP cadet, cross only when he signals you to do so.</li>
                                                <li style="line-height: 1.5;">When crossing between vehicles parked on the side of the road, remember that you are not visible to the moving traffic (because the parked vehicles may be taller than you). Stop as you appear from behind the vehicle and look for a safe gap before crossing. Remember, drivers need plenty of time to see you and to slow down and stop.</li>
                                                <li style="line-height: 1.5;">While crossing wide roads that have central islands, always cross in two stages. Cross to the central Island , stop, and cross when the next section is clear.</li>
                                                <li style="line-height: 1.5;">Never cross a road at a corner/curve, as the motorist taking the turn will not be able to see you in time.</li>
                                                <li style="line-height: 1.5;">Running across the road is a bad idea, as you may slip and fall.</li>
                                            
                                                </ul>
                                            
                                            </div>
                                                    
                                                    <br>
                                           <h2 style=" float: left;">&#9733;</h2>
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; width:100%; height: 350px; line-height: 1.5; text-align: justify; ">While going by bus
                                                
                                                <ul>
                                                    
                                                    <li style="line-height: 1.5;">Leave home well in time, so that you won’t have to run to catch the bus.</li>
                                                    <li style="line-height: 1.5;">At the bus stand, always follow the queue. Board the bus only after it has come to a halt, without rushing in or pushing others.</li>
                                                    <li style="line-height:1.5;">While in the bus, shouting or making a noise is definitely bad manners. Such behavior can also distract the driver.</li>                                                                                                                                                            
                                                    <li style="line-height: 1.5;">Do not board or alight at a bus stop other than the one decided by the school. Never board and alight at a red light crossing or unauthorized bus stop.</li>
                                                    <li style="line-height: 1.5;">Always hold onto the handrail if standing in a moving bus, especially on sharp turns.</li>
                                                    <li style="line-height: 1.5;">Do not sit, stand or travel on the footboard of the bus.</li>
                                                    <li style="line-height: 1.5;">Do not put any part of your body outside a moving or stationary bus.</li>
                                                    <li style="line-height: 1.5;">Always adhere to the bus safety rules.</li>
                                                </ul>  
                                                
                                                
                                                
                                                
                                            </div>
                                               
                                           <br> <h2 style=" float: left;">&#9733;</h2>
                                               <div style=" margin:  1% 0% 0% 5%; font-size: 20px; width:100%; height: 350px; line-height: 1.5; text-align: justify; ">
                                                Essentials for a School Bus as ordered by the Hon’ble Supreme Court of India
                                                    
                                                    <ul>
                                                        
                                                        <li style="line-height:1.5;">School Bus must be written on the back and front of the Bus.</li>
                                                        <li style="line-height:1.5;">If it is a hired bus, “On School Duty” should be clearly indicated</li>
                                                        <li style="line-height:1.5;">Bus must have a First-Aid-Box.</li>
                                                        <li style="line-height:1.5;">The windows of Bus must be fitted with horizontal grills.</li>
                                                        <li style="line-height:1.5;">There must be a Fire Extinguisher in the Bus.</li>
                                                        <li style="line-height:1.5;">School Name and Telephone No. must be written on the Bus.</li>
                                                        <li style="line-height:1.5;">The doors of the Bus should be fitted with reliable locks.</li>
                                                        <li style="line-height:1.5;">To keep School Bags safely, there should be a space fitted under the seats.</li>
                                                        <li style="line-height:1.5;">There must be an Attendant from the School in the Bus.</li>
                                                        <li style="line-height:1.5;">Any parent/guardian or a teacher may also travel to ensure these safety norms.</li>
                                                        
