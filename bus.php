                                            <h2 style=" float: left;">&#9733;</h2>
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px;  width:100%; line-height: 1.5; text-align: justify; ">
                                            
                                            Bus commuters should never board or deboard a moving bus. Maintaining a queue while boarding the bus will help avoid unnecessary hustle and bustle and will also save time. Once inside the bus keep your calm avoid shouting or making noise that would distract the driver, always hold onto the handrail if standing in a moving bus, stay away from the footboard of the bus and never put any part of the body outside a moving or stationary bus.
                                            
                                            </div>
                                            
                                            <br><h2 style=" float: left;">&#9733;</h2>
                                     
                                            <div style=" margin:  1% 0% 0% 5%; font-size: 20px; height: 370px;   width:100%; line-height: 1.5; text-align: justify; ">Commuters should observe the following guidelines when traveling on the bus :
                                               
                                                <li style="line-height: 1.5;">Commuters should not distract the bus driver while the bus in motion. All queries and requests are to be directed to the bus driver prior to travel or at the end of the journey.</li>
                                                <li style="line-height: 1.5;">Commuters should conduct themselves in a respectable and responsible manner at all times ensuring that their behaviour does not endanger the comfort and safety of their fellow passengers or the bus driver.</li>
                                                <li style="line-height: 1.5;">The bus driver should not make any unscheduled stops in the course of travel.</li>
                                                <li style="line-height: 1.5;">Commuters who breach these guidelines or who in the opinion of the bus driver have not acted in a responsible or respectable manner may be refused travel in the future.</li>	
                                                
